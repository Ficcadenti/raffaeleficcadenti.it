      <h3>NuSTAR</h3>
      <p>
        L'Anomalia del Sud Atlantico o SAA (South Atlantic Anomaly) è una zona del campo geomagnetico caratterizzata da un valore di intensità magnetica inferiore rispetto al campo medio generato dal dipolo magnetico; attualmente la SAA comprende la maggior parte del Sud Atlantico e parti del Sud America, Sud Africa e Antartide. L'Anomalia del Sud Atlantico produce importanti conseguenze per i satelliti artificiali e per altri veicoli spaziali che orbitano intorno alla Terra. Questi oggetti, muovendosi a diverse centinaia di chilometri dalla superficie terrestre, finiscono per transitare periodicamente all'interno della SAA; quando ciò accade, essi si trovano esposti a forti radiazioni per la durata di parecchi minuti. In quest'ambito in collaborazione con gli scienziati del team abbiamo sviluppato degli algoritmi (C++/Perl) per la missione NuSTAR, in grado di intercettare e depurare i segnali da queste anomalie magnetiche.
